function SortBy(){
    var selected = document.querySelector("#sortby").value;
    if(selected == "Low"){
        data.sort(function(a,b){
            return Number(a.price) - Number(b.price);
        })
    }
    if(selected == "High"){
        data.sort(function(a,b){
            return Number(b.price) - Number(a.price);
        })
    }
    displayItems(data);
}

